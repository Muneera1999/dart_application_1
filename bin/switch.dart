void main(){
  var dayofweek = 12;
  switch(dayofweek){
    case 1:
    print("sun");
    break;
    case 2:
    print('mon');
    break;
    case 3:
    print('tue');
    break;
    case 4:
    print('wed');
    break;
    case 5:
    print('thu');
    break;
    case 6:
    print('fri');
    break;
    case 7:
    print('sat');
    break;
    default:
    print('invalid');
    break;
  }
}